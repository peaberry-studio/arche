func runRemoteScript(ctx context.Context, ui *ui, ip, password, script string) error {
	sshPath := findSSH()
	if sshPath == "" {
		return errors.New("local ssh client not found")
	}
	askpassPath, err := createAskpassScript()
	if err != nil {
		return err
	}
	defer os.Remove(askpassPath)

	cmd := exec.CommandContext(ctx, sshPath,
		"-o", "StrictHostKeyChecking=no",
		"-o", "UserKnownHostsFile=/dev/null",
		"-o", "LogLevel=ERROR",
		"-o", "PreferredAuthentications=password",
		"-o", "PubkeyAuthentication=no",
		"-o", "KbdInteractiveAuthentication=no",
		"-o", "PasswordAuthentication=yes",
		"-o", "NumberOfPasswordPrompts=1",
		"-o", "ConnectTimeout=15",
		"root@"+ip,
		"bash -se",
	)
	cmd.Stdin = strings.NewReader(script)
	cmd.Env = append(os.Environ(),
		"DISPLAY=archectl",
		"SSH_ASKPASS="+askpassPath,
		"SSH_ASKPASS_REQUIRE=force",
		"ARCHE_SSH_PASSWORD="+password,
	)
	cmd.SysProcAttr = &syscall.SysProcAttr{Setpgid: true}

	stdout, err := cmd.StdoutPipe()
	if err != nil {
		return err
	}
	stderr, err := cmd.StderrPipe()
	if err != nil {
		return err
	}
	if err := cmd.Start(); err != nil {
		return err
	}

	progressDone := make(chan struct{})
	if !ui.verbose {
		started := time.Now()
		go func() {
			ticker := time.NewTicker(2 * time.Second)
			defer ticker.Stop()
			for {
				select {
				case <-progressDone:
					return
				case <-ticker.C:
					ui.progressWait("Remote update", time.Since(started), 10*time.Minute, "pulling images and restarting web")
				}
			}
		}()
	}

	var wg sync.WaitGroup
	scan := func(r io.Reader) {
		defer wg.Done()
		scanner := bufio.NewScanner(r)
		for scanner.Scan() {
			line := strings.TrimSpace(scanner.Text())
			if line != "" {
				ui.remote(line)
			}
		}
	}
	wg.Add(2)
	go scan(stdout)
	go scan(stderr)
	err = cmd.Wait()
	wg.Wait()
	close(progressDone)
	if err == nil && !ui.verbose {
		ui.progressDone("Remote update", 10*time.Minute, "web restarted")
	}
	return err
}

type remoteStreamer struct {
	ui          *ui
	ip          string
	password    string
	sshPath     string
	cancel      context.CancelFunc
	attached    chan struct{}
	done        chan struct{}
	mu          sync.Mutex
	complete    bool
	lastError   string
	askpassPath string
}

func newRemoteStreamer(ui *ui, ip, password string) *remoteStreamer {
	return &remoteStreamer{ui: ui, ip: ip, password: password, sshPath: findSSH(), attached: make(chan struct{}), done: make(chan struct{})}
}

func (s *remoteStreamer) available() bool {
	return s.sshPath != ""
}

func (s *remoteStreamer) bootComplete() bool {
	s.mu.Lock()
	defer s.mu.Unlock()
	return s.complete
}

func (s *remoteStreamer) start(parent context.Context) error {
	if s.sshPath == "" {
		return errors.New("ssh not found")
	}
	ctx, cancel := context.WithCancel(parent)
	s.cancel = cancel
	path, err := createAskpassScript()
	if err != nil {
		return err
	}
	s.askpassPath = path
	go s.run(ctx)
	return nil
}

func (s *remoteStreamer) waitUntilAttached(timeout time.Duration) bool {
	select {
	case <-s.attached:
		return true
	case <-time.After(timeout):
		return false
	}
}

func (s *remoteStreamer) stop() {
	if s.cancel != nil {
		s.cancel()
	}
	select {
	case <-s.done:
	case <-time.After(5 * time.Second):
	}
	if s.askpassPath != "" {
		_ = os.Remove(s.askpassPath)
	}
}

func (s *remoteStreamer) run(ctx context.Context) {
	defer close(s.done)
	defer func() {
		if s.askpassPath != "" {
			_ = os.Remove(s.askpassPath)
		}
	}()
	attachedClosed := false
	for ctx.Err() == nil {
		cmd := exec.CommandContext(ctx, s.sshPath,
			"-o", "StrictHostKeyChecking=no",
			"-o", "UserKnownHostsFile=/dev/null",
			"-o", "LogLevel=ERROR",
			"-o", "PreferredAuthentications=password",
			"-o", "PubkeyAuthentication=no",
			"-o", "KbdInteractiveAuthentication=no",
			"-o", "PasswordAuthentication=yes",
			"-o", "NumberOfPasswordPrompts=1",
			"-o", "ConnectTimeout=10",
			"root@"+s.ip,
			fmt.Sprintf("sh -lc 'touch /var/log/%s-bootstrap.log && tail -n +1 -F /var/log/%s-bootstrap.log'", appName, appName),
		)
		cmd.Stdin = nil
		cmd.Stderr = nil
		cmd.Env = append(os.Environ(),
			"DISPLAY=arche-installer",
			"SSH_ASKPASS="+s.askpassPath,
			"SSH_ASKPASS_REQUIRE=force",
			"ARCHE_SSH_PASSWORD="+s.password,
		)
		cmd.SysProcAttr = &syscall.SysProcAttr{Setpgid: true}
		stdout, err := cmd.StdoutPipe()
		if err != nil {
			s.setError(err.Error())
			return
		}
		if err := cmd.Start(); err != nil {
			s.setError(err.Error())
			return
		}
		if !attachedClosed {
			close(s.attached)
			attachedClosed = true
		}
		scanner := bufio.NewScanner(stdout)
		for scanner.Scan() {
			line := strings.TrimSpace(scanner.Text())
			if line == "" {
				continue
			}
			s.ui.remote(line)
			if strings.Contains(line, "Bootstrap complete") {
				s.mu.Lock()
				s.complete = true
				s.mu.Unlock()
			}
		}
		err = cmd.Wait()
		if ctx.Err() != nil || s.bootComplete() {
			return
		}
		if err != nil {
			s.setError(err.Error())
		}
		s.ui.wait("Remote bootstrap log stream dropped. Retrying in 5s.")
		select {
		case <-ctx.Done():
			return
		case <-time.After(5 * time.Second):
		}
	}
}

func (s *remoteStreamer) setError(message string) {
	s.mu.Lock()
	defer s.mu.Unlock()
	s.lastError = message
}

func createAskpassScript() (string, error) {
	file, err := os.CreateTemp("", "arche-askpass-*.sh")
	if err != nil {
		return "", err
	}
	path := file.Name()
	if _, err := file.WriteString("#!/bin/sh\nprintf '%s\\n' \"$ARCHE_SSH_PASSWORD\"\n"); err != nil {
		_ = file.Close()
		return "", err
	}
	if err := file.Close(); err != nil {
		return "", err
	}
	if err := os.Chmod(path, 0o700); err != nil {
		return "", err
	}
	return path, nil
}

func findSSH() string {
	path, err := exec.LookPath("ssh")
	if err != nil {
		return ""
	}
	return path
}

