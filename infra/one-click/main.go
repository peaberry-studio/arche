package main

/*
Changes made:
- Replaced the Python one-click deployer with a Go CLI so the public installer
  can ship as a single compiled binary with no Python, pip, or runtime package
  setup on user laptops.
- Kept install.sh as the curl|bash entrypoint; it detects OS/arch, installs
  the matching archectl binary locally, and runs the requested lifecycle command.
- Added install, update, and destroy commands. Install/update accept a version
  tag and use it for both GHCR web and workspace images.
- Preserved the narrow-path deployment behavior: DigitalOcean only,
  autogenerated secrets, autogenerated nip.io hostname, Docker,
  PostgreSQL, Traefik, docker-socket-proxy, persistent KB/user mounts, public
  health polling, local credential file, and inline remote bootstrap logs.

Language choice: Go, because this installer is meant to run on arbitrary user
laptops and a static-ish single binary is more reliable than bootstrapping a
Python runtime plus terminal UI dependencies.
*/

