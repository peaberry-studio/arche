func printSummary(ui *ui, result artifacts, health *healthResult) {
	status := "bootstrapping"
	tone := "warn"
	healthWait := "skipped"
	lastCheck := "-"
	if health != nil {
		healthWait = formatDuration(time.Duration(health.ElapsedSeconds) * time.Second)
		lastCheck = health.LastStatus
		if health.Ready {
			status = "ready"
			tone = "success"
		}
	}
	ui.print("")
	ui.panel("Deployment complete", [][2]string{
		{"Status", status},
		{"Server IP", result.IPAddress},
		{"App URL", result.PublicURL},
		{"Credentials", result.CredentialsFile},
		{"JSON record", result.JSONFile},
		{"Health wait", healthWait},
		{"Last check", lastCheck},
	}, tone)
	if health == nil || !health.Ready {
		ui.warn("The server may still be finishing first boot. Keep the bootstrap log open if the URL is not ready yet.")
	} else {
		ui.ok("Arche answered the public health check and is ready to open at: "+result.PublicURL, time.Time{})
	}
}

