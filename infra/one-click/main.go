func waitForSSHPort(ctx context.Context, ip string, ui *ui) bool {
	started := time.Now()
	timeout := 5 * time.Minute
	last := "connection pending"
	for {
		conn, err := net.DialTimeout("tcp", net.JoinHostPort(ip, "22"), 5*time.Second)
		if err == nil {
			_ = conn.Close()
			ui.progressDone("SSH availability", timeout, "port=22 | ready")
			return true
		}
		last = err.Error()
		ui.progressWait("SSH availability", time.Since(started), timeout, "port=22 | last="+last)
		if time.Since(started) > timeout {
			return false
		}
		select {
		case <-ctx.Done():
			return false
		case <-time.After(10 * time.Second):
		}
	}
}

func waitForPublicHealth(ctx context.Context, publicURL string, ui *ui, timeout time.Duration) (healthResult, error) {
	target := publicURL + "/api/health"
	client := &http.Client{Timeout: 10 * time.Second, Transport: &http.Transport{TLSClientConfig: &tls.Config{MinVersion: tls.VersionTLS12}}}
	started := time.Now()
	attempt := 0
	last := "waiting for first successful health check"
	for time.Since(started) < timeout {
		attempt++
		req, _ := http.NewRequestWithContext(ctx, http.MethodGet, target, nil)
		resp, err := client.Do(req)
		if err == nil {
			_ = resp.Body.Close()
			if resp.StatusCode == http.StatusOK {
				ui.progressDone("Public health check", timeout, fmt.Sprintf("attempt=%d | last=HTTP 200 | remaining=0s", attempt))
				return healthResult{Ready: true, ElapsedSeconds: int(time.Since(started).Seconds()), LastStatus: "HTTP 200"}, nil
			}
			last = fmt.Sprintf("HTTP %d", resp.StatusCode)
		} else {
			last = err.Error()
			var urlErr *url.Error
			if errors.As(err, &urlErr) {
				last = urlErr.Err.Error()
			}
		}
		remaining := max(time.Duration(0), timeout-time.Since(started))
		ui.progressWait("Public health check", time.Since(started), timeout, fmt.Sprintf("attempt=%d | last=%s | remaining=%s", attempt, last, formatDuration(remaining)))
		select {
		case <-ctx.Done():
			return healthResult{Ready: false, ElapsedSeconds: int(time.Since(started).Seconds()), LastStatus: last}, ctx.Err()
		case <-time.After(15 * time.Second):
		}
	}
	return healthResult{Ready: false, ElapsedSeconds: int(time.Since(started).Seconds()), LastStatus: last}, fmt.Errorf("timed out waiting for public health check. Last status: %s", last)
}

func runUpdate(ctx context.Context, ui *ui, args cliArgs) int {
	state, statePath, err := stateForUpdate(args)
	if err != nil {
		fmt.Fprintln(os.Stderr, "Error:", err)
		return 1
	}
	token, err := collectDOToken(args.token)
	if err != nil {
		fmt.Fprintln(os.Stderr, "Error:", err)
		return 1
	}

	webImage := appImageForVersion(args.version)
	workspaceImage := workspaceImageForVersion(args.version)
	ui.panel("Update plan", [][2]string{
		{"State file", statePath},
		{"Server IP", state.Deployment.IPAddress},
		{"App URL", state.Deployment.PublicURL},
		{"From version", fallback(state.Version, "unknown")},
		{"To version", args.version},
		{"Web image", webImage},
		{"Workspace image", workspaceImage},
	}, "info")

	if err := ensureSSHKnownHost(ctx, ui, &state); err != nil {
		fmt.Fprintln(os.Stderr, "Error:", err)
		return 1
	}

	client := newDOClient(token)
	started := ui.stage("Validating DigitalOcean API token")
	if err := client.validateToken(ctx); err != nil {
		if errors.Is(err, context.Canceled) {
			ui.warn("Cancelled by user.")
			return 130
		}
		fmt.Fprintln(os.Stderr, "Error:", err)
		return 1
	}
	ui.ok("DigitalOcean API token accepted", started)

	started = ui.stage("Updating remote Compose configuration")
	script := renderUpdateScript(args.version, renderCompose())
	if err := runRemoteScript(ctx, ui, sshTarget{ip: state.Deployment.IPAddress, user: sshUserForState(state), auth: sshAuthForState(state)}, script); err != nil {
		if errors.Is(err, context.Canceled) {
			ui.warn("Cancelled by user.")
			return 130
		}
		fmt.Fprintln(os.Stderr, "Error:", err)
		return 1
	}
	ui.ok("Remote stack updated to "+args.version, started)

	state.Version = args.version
	state.AppImage = webImage
	state.WorkspaceImage = workspaceImage
	state.UpdatedAt = time.Now().UTC().Format(time.RFC3339)
	writePath := statePath
	if writePath == "manual flags" {
		writePath, err = defaultStatePath()
		if err != nil {
			fmt.Fprintln(os.Stderr, "Error:", err)
			return 1
		}
		state.Deployment.JSONFile = writePath
	}
	if err := writeStateFiles(state, writePath); err != nil {
		fmt.Fprintln(os.Stderr, "Error:", err)
		return 1
	}

	if args.skipHealthWait {
		ui.warn("Skipping public health wait by request.")
		printSummary(ui, state.Deployment, nil)
		return 0
	}

	started = ui.stage("Waiting for updated public health check")
	health, err := waitForPublicHealth(ctx, state.Deployment.PublicURL, ui, defaultHealthWait)
	if err != nil {
		if errors.Is(err, context.Canceled) {
			ui.warn("Cancelled by user.")
			return 130
		}
		ui.warn(err.Error(), started)
	} else if health.Ready {
		ui.ok("Updated version answered the public health check", started)
	}
	printSummary(ui, state.Deployment, &health)
	return 0
}

func stateForUpdate(args cliArgs) (deploymentState, string, error) {
	if args.ipAddress != "" || args.sshPassword != "" || args.sshKeyPath != "" {
		if args.ipAddress == "" {
			return deploymentState{}, "", errors.New("--ip is required when using manual SSH recovery flags")
		}
		if args.sshPassword != "" && args.sshKeyPath != "" {
			return deploymentState{}, "", errors.New("provide either --ssh-key or --ssh-password, not both")
		}
		if args.sshPassword == "" && args.sshKeyPath == "" {
			return deploymentState{}, "", errors.New("manual recovery requires --ssh-key or --ssh-password")
		}
		privateKey := ""
		if args.sshKeyPath != "" {
			key, err := readSSHPrivateKey(args.sshKeyPath)
			if err != nil {
				return deploymentState{}, "", err
			}
			privateKey = key
		}
		publicURL := strings.TrimSpace(args.publicURL)
		if publicURL == "" {
			publicURL = "https://" + buildDomainFromIP(args.ipAddress)
		}
		return deploymentState{
			AppName:        appName,
			Version:        defaultVersion,
			AppImage:       appImageForVersion(defaultVersion),
			WorkspaceImage: workspaceImageForVersion(defaultVersion),
			DBName:         dbName,
			Deployment: artifacts{
				IPAddress: args.ipAddress,
				Domain:    strings.TrimPrefix(strings.TrimPrefix(publicURL, "https://"), "http://"),
				PublicURL: publicURL,
			},
			Secrets: stateSecrets{RootPassword: args.sshPassword, SSHPrivateKey: privateKey},
		}, "manual flags", nil
	}
	return readState(args.statePath)
}

