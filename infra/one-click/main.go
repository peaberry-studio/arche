func ensureSSHKnownHost(ctx context.Context, ui *ui, state *deploymentState) error {
	if strings.TrimSpace(state.Secrets.SSHKnownHost) != "" || strings.TrimSpace(state.Deployment.IPAddress) == "" {
		return nil
	}
	if findSSHKeyscan() == "" {
		ui.warn("ssh-keyscan is not available locally. Falling back to accept-new host key trust for this update.")
		return nil
	}
	started := ui.stage("Pinning the server SSH host key")
	knownHost, err := scanSSHHostKey(ctx, state.Deployment.IPAddress)
	if err != nil {
		if errors.Is(err, context.Canceled) {
			return err
		}
		ui.warn("Could not pre-pin the SSH host key: "+err.Error(), started)
		return nil
	}
	state.Secrets.SSHKnownHost = knownHost
	ui.ok("Pinned the server SSH host key", started)
	return nil
}

func runRemoteScript(ctx context.Context, ui *ui, target sshTarget, script string) error {
	sshPath := findSSH()
	if sshPath == "" {
		return errors.New("local ssh client not found")
	}
	args, env, cleanup, err := prepareSSHInvocation(target.auth, 15, "archectl")
	if err != nil {
		return err
	}
	defer cleanup()

	cmdArgs := append(args, target.login(), target.stdinScriptCommand())
	cmd := exec.CommandContext(ctx, sshPath, cmdArgs...)
	cmd.Stdin = strings.NewReader(script)
	cmd.Env = env
	cmd.SysProcAttr = &syscall.SysProcAttr{Setpgid: true}

	stdout, err := cmd.StdoutPipe()
	if err != nil {
		return err
	}
	stderr, err := cmd.StderrPipe()
	if err != nil {
		return err
	}
	if err := cmd.Start(); err != nil {
		return err
	}

	progressDone := make(chan struct{})
	if !ui.verbose {
		started := time.Now()
		go func() {
			ticker := time.NewTicker(2 * time.Second)
			defer ticker.Stop()
			for {
				select {
				case <-progressDone:
					return
				case <-ticker.C:
					ui.progressWait("Remote update", time.Since(started), 10*time.Minute, "pulling images and restarting web")
				}
			}
		}()
	}

	var wg sync.WaitGroup
	scan := func(r io.Reader) {
		defer wg.Done()
		scanner := bufio.NewScanner(r)
		for scanner.Scan() {
			line := strings.TrimSpace(scanner.Text())
			if line != "" {
				ui.remote(line)
			}
		}
	}
	wg.Add(2)
	go scan(stdout)
	go scan(stderr)
	err = cmd.Wait()
	wg.Wait()
	close(progressDone)
	if err == nil && !ui.verbose {
		ui.progressDone("Remote update", 10*time.Minute, "web restarted")
	}
	return err
}

type remoteStreamer struct {
	ui        *ui
	target    sshTarget
	sshPath   string
	cancel    context.CancelFunc
	attached  chan struct{}
	done      chan struct{}
	cleanup   func()
	sshArgs   []string
	sshEnv    []string
	mu        sync.Mutex
	complete  bool
	lastError string
}

func newRemoteStreamer(ui *ui, target sshTarget) *remoteStreamer {
	return &remoteStreamer{ui: ui, target: target, sshPath: findSSH(), attached: make(chan struct{}), done: make(chan struct{})}
}

func (s *remoteStreamer) available() bool {
	return s.sshPath != ""
}

func (s *remoteStreamer) bootComplete() bool {
	s.mu.Lock()
	defer s.mu.Unlock()
	return s.complete
}

func (s *remoteStreamer) start(parent context.Context) error {
	if s.sshPath == "" {
		return errors.New("ssh not found")
	}
	args, env, cleanup, err := prepareSSHInvocation(s.target.auth, 10, "arche-installer")
	if err != nil {
		return err
	}
	ctx, cancel := context.WithCancel(parent)
	s.cancel = cancel
	s.cleanup = cleanup
	s.sshArgs = args
	s.sshEnv = env
	go s.run(ctx)
	return nil
}

func (s *remoteStreamer) waitUntilAttached(timeout time.Duration) bool {
	select {
	case <-s.attached:
		return true
	case <-time.After(timeout):
		return false
	}
}

func (s *remoteStreamer) stop() {
	if s.cancel != nil {
		s.cancel()
	}
	select {
	case <-s.done:
	case <-time.After(5 * time.Second):
	}
	if s.cleanup != nil {
		s.cleanup()
	}
}

func (s *remoteStreamer) run(ctx context.Context) {
	defer close(s.done)
	attachedClosed := false
	for ctx.Err() == nil {
		cmdArgs := append(append([]string{}, s.sshArgs...), s.target.login(), s.target.streamCommand(fmt.Sprintf("touch /var/log/%s-bootstrap.log && tail -n +1 -F /var/log/%s-bootstrap.log", appName, appName)))
		cmd := exec.CommandContext(ctx, s.sshPath, cmdArgs...)
		cmd.Stdin = nil
		cmd.Stderr = nil
		cmd.Env = s.sshEnv
		cmd.SysProcAttr = &syscall.SysProcAttr{Setpgid: true}
		stdout, err := cmd.StdoutPipe()
		if err != nil {
			s.setError(err.Error())
			return
		}
		if err := cmd.Start(); err != nil {
			s.setError(err.Error())
			return
		}
		if !attachedClosed {
			close(s.attached)
			attachedClosed = true
		}
		scanner := bufio.NewScanner(stdout)
		for scanner.Scan() {
			line := strings.TrimSpace(scanner.Text())
			if line == "" {
				continue
			}
			s.ui.remote(line)
			if strings.Contains(line, "Bootstrap complete") {
				s.mu.Lock()
				s.complete = true
				s.mu.Unlock()
			}
		}
		err = cmd.Wait()
		if ctx.Err() != nil || s.bootComplete() {
			return
		}
		if err != nil {
			s.setError(err.Error())
		}
		s.ui.wait("Remote bootstrap log stream dropped. Retrying in 5s.")
		select {
		case <-ctx.Done():
			return
		case <-time.After(5 * time.Second):
		}
	}
}

func (s *remoteStreamer) setError(message string) {
	s.mu.Lock()
	defer s.mu.Unlock()
	s.lastError = message
}

