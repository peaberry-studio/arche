func validateTemplates(version string) error {
	sample := input{token: "sample", email: "admin@example.com", size: defaultDropletSize, region: defaultDropletRegion, version: version}
	secret := generateSecrets()
	envTemplate := renderEnvTemplate(sample, secret)
	compose := renderCompose()
	envKeys := map[string]bool{}
	for _, line := range strings.Split(envTemplate, "\n") {
		line = strings.TrimSpace(line)
		if line == "" || strings.HasPrefix(line, "#") || !strings.Contains(line, "=") {
			continue
		}
		envKeys[strings.SplitN(line, "=", 2)[0]] = true
	}
	re := regexp.MustCompile(`\$\{([A-Z0-9_]+)(?::[^}]*)?\}`)
	missing := map[string]bool{}
	for _, match := range re.FindAllStringSubmatch(compose, -1) {
		if len(match) > 1 && !envKeys[match[1]] {
			missing[match[1]] = true
		}
	}
	if len(missing) > 0 {
		keys := make([]string, 0, len(missing))
		for key := range missing {
			keys = append(keys, key)
		}
		sort.Strings(keys)
		return fmt.Errorf("docker-compose references env vars missing from .env: %s", strings.Join(keys, ", "))
	}
	if !strings.Contains(envTemplate, envPlaceholderDomain) || !strings.Contains(envTemplate, envPlaceholderPublicBaseURL) {
		return errors.New("dynamic domain placeholders are missing from generated .env template")
	}
	return nil
}

