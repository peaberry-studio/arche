func newUI() *ui {
	width := 92
	if terminalWidth, _, err := term.GetSize(int(os.Stdout.Fd())); err == nil {
		width = min(max(72, terminalWidth-4), 108)
	}
	return &ui{
		width:       width,
		interactive: term.IsTerminal(int(os.Stdout.Fd())),
		styles: uiStyles{
			title:     lipgloss.NewStyle().Bold(true).Foreground(lipgloss.Color("#F5F5F0")),
			subtitle:  lipgloss.NewStyle().Foreground(lipgloss.Color("#6EE7D8")),
			panel:     lipgloss.NewStyle().Border(lipgloss.RoundedBorder()).BorderForeground(lipgloss.Color("#6EE7D8")).Padding(1, 2).Width(width),
			panelWarn: lipgloss.NewStyle().Border(lipgloss.RoundedBorder()).BorderForeground(lipgloss.Color("#EAB308")).Padding(1, 2).Width(width),
			panelOK:   lipgloss.NewStyle().Border(lipgloss.RoundedBorder()).BorderForeground(lipgloss.Color("#22C55E")).Padding(1, 2).Width(width),
			muted:     lipgloss.NewStyle().Foreground(lipgloss.Color("#737373")),
			cyan:      lipgloss.NewStyle().Foreground(lipgloss.Color("#6EE7D8")),
			green:     lipgloss.NewStyle().Foreground(lipgloss.Color("#22C55E")),
			yellow:    lipgloss.NewStyle().Foreground(lipgloss.Color("#EAB308")),
			bold:      lipgloss.NewStyle().Bold(true),
		},
	}
}

func (u *ui) banner(title, subtitle string) {
	u.print(u.styles.panel.Render(lipgloss.JoinVertical(lipgloss.Left, u.styles.title.Render(title), u.styles.subtitle.Render(subtitle))))
}

func (u *ui) stage(title string) time.Time {
	u.step++
	u.log("STEP", u.styles.cyan, fmt.Sprintf("%02d  %s", u.step, u.styles.bold.Render(title)))
	return time.Now()
}

func (u *ui) info(message string) {
	u.log("INFO", u.styles.cyan, message)
}

func (u *ui) wait(message string) {
	u.log("WAIT", u.styles.cyan, message)
}

func (u *ui) ok(message string, started time.Time) {
	u.log("OK", u.styles.green, withDuration(message, started))
}

func (u *ui) warn(message string, started ...time.Time) {
	text := message
	if len(started) > 0 {
		text = withDuration(message, started[0])
	}
	u.log("WARN", u.styles.yellow, text)
}

func (u *ui) command(label, command string) {
	u.log("CMD", u.styles.muted, u.styles.bold.Render(label+": ")+u.styles.muted.Render(command))
}

func (u *ui) remote(message string) {
	if !u.verbose {
		return
	}
	u.log("REMOTE", u.styles.muted, u.styles.muted.Render(message))
}

func (u *ui) panel(title string, rows [][2]string, tone string) {
	u.mu.Lock()
	defer u.mu.Unlock()
	u.finishProgressLocked()

	style := u.styles.cyan.Bold(true)
	if tone == "success" {
		style = u.styles.green.Bold(true)
	}
	if tone == "warn" {
		style = u.styles.yellow.Bold(true)
	}
	maxKey := 0
	for _, row := range rows {
		maxKey = max(maxKey, len(row[0]))
	}
	fmt.Println(style.Render(title))
	for _, row := range rows {
		key := u.styles.muted.Render(row[0] + strings.Repeat(" ", maxKey-len(row[0])))
		fmt.Println("  " + key + "  " + row[1])
	}
}

func (u *ui) progress(label string, current, total time.Duration, details string) string {
	ratio := 0.0
	if total > 0 {
		ratio = math.Max(0, math.Min(1, float64(current)/float64(total)))
	}
	width := 22
	filled := int(math.Round(ratio * float64(width)))
	bar := u.styles.cyan.Render(strings.Repeat("━", filled)) + u.styles.muted.Render(strings.Repeat("━", width-filled))
	return fmt.Sprintf("%s  %s  %3d%%  %s", u.styles.cyan.Bold(true).Render(label), bar, int(ratio*100), details)
}

func (u *ui) log(label string, style lipgloss.Style, message string) {
	timestamp := u.styles.muted.Render(time.Now().Format("15:04:05"))
	u.print(timestamp + " " + style.Render(label) + " " + message)
}

func (u *ui) print(lines ...string) {
	u.mu.Lock()
	defer u.mu.Unlock()
	u.finishProgressLocked()
	for _, line := range lines {
		fmt.Println(line)
	}
}

func (u *ui) progressWait(label string, current, total time.Duration, details string) {
	line := u.styles.muted.Render(time.Now().Format("15:04:05")) + " " + u.styles.cyan.Render("WAIT") + " " + u.progress(label, current, total, details)
	u.mu.Lock()
	defer u.mu.Unlock()
	if !u.interactive {
		fmt.Println(line)
		return
	}
	padding := ""
	plainWidth := lipgloss.Width(line)
	if u.progressWidth > plainWidth {
		padding = strings.Repeat(" ", u.progressWidth-plainWidth)
	}
	fmt.Print("\r" + line + padding)
	u.progressWidth = plainWidth
	u.progressActive = true
}

func (u *ui) progressDone(label string, total time.Duration, details string) {
	u.progressWait(label, total, total, details)
	u.mu.Lock()
	defer u.mu.Unlock()
	u.finishProgressLocked()
}

func (u *ui) finishProgressLocked() {
	if !u.progressActive {
		return
	}
	fmt.Println()
	u.progressActive = false
	u.progressWidth = 0
}

