func main() {
	ui := newUI()
	ctx, stopSignals := signal.NotifyContext(context.Background(), os.Interrupt)
	defer stopSignals()

	code := run(ctx, ui)
	if code != 0 {
		os.Exit(code)
	}
}

func run(ctx context.Context, ui *ui) int {
	args, err := parseArgs(os.Args[1:])
	if err != nil {
		fmt.Fprintln(os.Stderr, "Error:", err)
		printUsage(os.Stderr)
		return 1
	}
	if args.help {
		printUsage(os.Stdout)
		return 0
	}
	ui.verbose = args.verbose

	if err := validateVersion(args.version); err != nil {
		fmt.Fprintln(os.Stderr, "Error:", err)
		return 1
	}
	if err := validateTemplates(args.version); err != nil {
		fmt.Fprintln(os.Stderr, "Error:", err)
		return 1
	}
	if args.validateOnly {
		ui.ok("Template validation passed.", time.Time{})
		return 0
	}

	switch args.command {
	case "install":
		return runInstall(ctx, ui, args)
	case "update":
		return runUpdate(ctx, ui, args)
	case "destroy":
		return runDestroy(ctx, ui, args)
	default:
		fmt.Fprintln(os.Stderr, "Error: unsupported command "+args.command)
		printUsage(os.Stderr)
		return 1
	}
}

func runInstall(ctx context.Context, ui *ui, args cliArgs) int {
	var streamer *remoteStreamer
	defer func() {
		if streamer != nil {
			streamer.stop()
			if streamer.lastError != "" && !streamer.bootComplete() {
				ui.warn("Inline remote log stream ended early: " + streamer.lastError)
			}
		}
	}()

	done := make(chan struct{})
	defer close(done)
	go func() {
		select {
		case <-ctx.Done():
			ui.warn("Cancelled by user.")
			if streamer != nil {
				streamer.stop()
			}
			os.Exit(130)
		case <-done:
		}
	}()

	inputData, err := collectInput(args, ui)
	if err != nil {
		fmt.Fprintln(os.Stderr, "Error:", err)
		return 1
	}

	started := ui.stage("Generating secrets and first-boot payload")
	secrets := generateSecrets()
	envTemplate := renderEnvTemplate(inputData, secrets)
	composeContent := renderCompose()
	bootstrapScript := renderBootstrapScript()
	cloudInit := renderCloudInit(secrets, envTemplate, composeContent, bootstrapScript)
	ui.ok("Generated runtime secrets, cloud-init, .env template, and docker-compose.yml", started)

	client := &doClient{token: inputData.token, http: &http.Client{Timeout: 30 * time.Second}}
	started = ui.stage("Validating DigitalOcean API token")
	if err := client.validateToken(ctx); err != nil {
		fmt.Fprintln(os.Stderr, "Error:", err)
		return 1
	}
	ui.ok("DigitalOcean API token accepted", started)

	started = ui.stage("Creating Droplet")
	dropletID, dropletName, err := createDroplet(ctx, client, inputData, cloudInit)
	if err != nil {
		fmt.Fprintln(os.Stderr, "Error:", err)
		return 1
	}
	ui.ok(fmt.Sprintf("Created Droplet %s (id %d)", dropletName, dropletID), started)

	started = ui.stage("Waiting for the Droplet to become active")
	ipAddress, err := waitForDropletIP(ctx, client, dropletID, ui)
	if err != nil {
		fmt.Fprintln(os.Stderr, "Error:", err)
		return 1
	}
	ui.ok("Droplet is active with public IPv4 "+ipAddress, started)

	started = ui.stage("Attaching firewall")
	firewallID, err := createFirewall(ctx, client, dropletID, dropletName)
	if err != nil {
		fmt.Fprintln(os.Stderr, "Error:", err)
		return 1
	}
	ui.ok("Attached firewall "+firewallID+" with ports 22, 80, and 443 open", started)

	domain := buildDomainFromIP(ipAddress)
	publicURL := "https://" + domain
	started = ui.stage("Writing local deployment record")
	textFile, jsonFile, err := allocateLocalPaths()
	if err != nil {
		fmt.Fprintln(os.Stderr, "Error:", err)
		return 1
	}
	result := artifacts{
		DropletID:       dropletID,
		FirewallID:      firewallID,
		DropletName:     dropletName,
		IPAddress:       ipAddress,
		Domain:          domain,
		PublicURL:       publicURL,
		AdminEmail:      inputData.email,
		CredentialsFile: textFile,
		JSONFile:        jsonFile,
	}
	if err := writeLocalFiles(inputData, secrets, result); err != nil {
		fmt.Fprintln(os.Stderr, "Error:", err)
		return 1
	}
	ui.ok("Saved deployment record to "+textFile, started)
	ui.info("Public URL: " + publicURL)
	if args.verbose {
		ui.command("Bootstrap logs", fmt.Sprintf("ssh root@%s 'tail -f /var/log/%s-bootstrap.log'", ipAddress, appName))
		ui.command("Compose logs", fmt.Sprintf("ssh root@%s 'cd /opt/%s && docker compose logs -f web'", ipAddress, appName))
	}

	if args.verbose {
		started = ui.stage("Opening the live remote bootstrap log")
		if waitForSSHPort(ctx, ipAddress, ui) {
			streamer = newRemoteStreamer(ui, ipAddress, secrets.rootPassword)
			if streamer.available() {
				if err := streamer.start(ctx); err != nil {
					ui.warn("Could not start inline remote log stream: "+err.Error(), started)
				} else if streamer.waitUntilAttached(15 * time.Second) {
					ui.ok("Streaming /var/log/arche-bootstrap.log inline", started)
				} else {
					ui.warn("SSH is reachable, but the live log stream has not attached yet. Health polling will continue.", started)
				}
			} else {
				ui.warn("Local ssh client not found. Falling back to manual log commands.", started)
			}
		} else {
			ui.warn("SSH did not open in time for the inline log stream. Falling back to manual log commands.", started)
		}
	}

	var health *healthResult
	if args.skipHealthWait {
		ui.warn("Skipping public health wait by request.")
	} else {
		started = ui.stage("Waiting for first boot, TLS, and the public health check")
		ui.info("This can take a few minutes while the server installs Docker, pulls images, and requests certificates.")
		result, err := waitForPublicHealth(ctx, publicURL, ui, defaultHealthWait)
		health = &result
		if err != nil {
			ui.warn(err.Error(), started)
		} else if result.Ready {
			ui.ok("Public health check passed for "+publicURL, started)
		}
	}

	printSummary(ui, result, health)
	return 0
}

func parseArgs(argv []string) (cliArgs, error) {
	args := cliArgs{command: "install", size: defaultDropletSize, region: defaultDropletRegion, version: defaultVersion}
	for len(argv) > 0 {
		if argv[0] == "-vv" || argv[0] == "--verbose" {
			args.verbose = true
			argv = argv[1:]
			continue
		}
		break
	}
	if len(argv) > 0 {
		switch argv[0] {
		case "install", "update", "destroy":
			args.command = argv[0]
			argv = argv[1:]
		case "help", "--help", "-h":
			args.help = true
			return args, nil
		default:
			if !strings.HasPrefix(argv[0], "-") {
				return args, fmt.Errorf("unknown command %q", argv[0])
			}
		}
	}

	flags := flag.NewFlagSet(args.command, flag.ContinueOnError)
	flags.SetOutput(io.Discard)
	flags.BoolVar(&args.help, "help", false, "Show help")
	flags.BoolVar(&args.help, "h", false, "Show help")
	flags.BoolVar(&args.validateOnly, "validate-only", false, "Render and validate generated templates, then exit")
	flags.BoolVar(&args.verbose, "vv", args.verbose, "Show SSH/bootstrap logs")
	flags.BoolVar(&args.verbose, "verbose", args.verbose, "Show SSH/bootstrap logs")
	flags.StringVar(&args.version, "version", defaultVersion, "Arche image version tag, for example v1.2.3 or latest")

	switch args.command {
	case "install":
		flags.StringVar(&args.token, "token", "", "DigitalOcean API token")
		flags.StringVar(&args.email, "email", "", "Email for the Arche admin account and Let's Encrypt")
		flags.StringVar(&args.size, "size", defaultDropletSize, "Droplet size slug")
		flags.StringVar(&args.region, "region", defaultDropletRegion, "Droplet region slug")
		flags.BoolVar(&args.skipHealthWait, "skip-health-wait", false, "Do not wait for the public URL to answer")
	case "update":
		flags.StringVar(&args.token, "token", "", "DigitalOcean API token")
		flags.StringVar(&args.statePath, "state", "", "Path to deployment state JSON")
		flags.StringVar(&args.ipAddress, "ip", "", "Server IPv4 address when no state file is available")
		flags.StringVar(&args.sshPassword, "ssh-password", "", "Root SSH password when no state file is available")
		flags.StringVar(&args.publicURL, "url", "", "Public app URL when no state file is available")
		flags.BoolVar(&args.skipHealthWait, "skip-health-wait", false, "Do not wait for the public URL to answer after update")
	case "destroy":
		flags.StringVar(&args.token, "token", "", "DigitalOcean API token")
		flags.StringVar(&args.statePath, "state", "", "Path to deployment state JSON")
		flags.IntVar(&args.dropletID, "droplet-id", 0, "DigitalOcean Droplet id when no state file is available")
		flags.StringVar(&args.firewallID, "firewall-id", "", "DigitalOcean firewall id when no state file is available")
		flags.BoolVar(&args.yes, "yes", false, "Destroy without interactive confirmation")
	default:
		return args, fmt.Errorf("unknown command %q", args.command)
	}

	if err := flags.Parse(argv); err != nil {
		return args, err
	}
	if flags.NArg() > 0 {
		return args, fmt.Errorf("unexpected argument %q", flags.Arg(0))
	}
	if args.command == "update" && args.version == defaultVersion {
		return args, errors.New("update requires --version vX.X.X")
	}
	return args, nil
}

func printUsage(w io.Writer) {
	fmt.Fprintln(w, `Usage:
  archectl install [--token TOKEN] --email EMAIL [--version vX.X.X] [-vv]
  archectl update --token TOKEN --version vX.X.X [--state PATH] [-vv]
  archectl destroy --token TOKEN [--state PATH] [--yes]

Recovery without a state file:
  archectl update --token TOKEN --version vX.X.X --ip IP --ssh-password PASSWORD [--url URL]
  archectl destroy --token TOKEN --droplet-id ID [--firewall-id ID] --yes

If no command is provided, archectl defaults to install for curl|bash compatibility.
State defaults to ~/.arche/deployments/current.json.
Use -vv or --verbose to show SSH/bootstrap logs.`)
}

func collectInput(args cliArgs, ui *ui) (input, error) {
	reader := bufio.NewReader(os.Stdin)
	token, err := collectDOToken(args.token)
	if err != nil {
		return input{}, err
	}

	email := strings.TrimSpace(args.email)
	if email == "" {
		email = strings.TrimSpace(os.Getenv("ARCHE_INSTALL_EMAIL"))
	}
	if email == "" {
		fmt.Print("Email for the Arche admin account and Let's Encrypt: ")
		raw, err := reader.ReadString('\n')
		if err != nil {
			return input{}, fmt.Errorf("read email: %w", err)
		}
		email = strings.TrimSpace(raw)
	}

	if token == "" {
		return input{}, errors.New("DigitalOcean API token is required")
	}
	emailPattern := regexp.MustCompile(`^[^@\s]+@[^@\s]+\.[^@\s]+$`)
	if !emailPattern.MatchString(email) {
		return input{}, errors.New("enter a valid email address")
	}

	ui.panel("Deployment plan", [][2]string{
		{"Droplet image", dropletImage},
		{"Droplet size", args.size},
		{"Droplet region", args.region},
		{"Version", args.version},
		{"Web image", appImageForVersion(args.version)},
		{"Workspace image", workspaceImageForVersion(args.version)},
		{"Public URL", "https://arche-<droplet-ip>.nip.io"},
	}, "info")
	fmt.Print("Continue and create the Droplet? (Y/n): ")
	answer, err := reader.ReadString('\n')
	if err != nil {
		return input{}, fmt.Errorf("read confirmation: %w", err)
	}
	answer = strings.ToLower(strings.TrimSpace(answer))
	if answer != "" && answer != "y" && answer != "yes" {
		return input{}, errors.New("deployment cancelled")
	}

	return input{token: token, email: email, size: args.size, region: args.region, version: args.version}, nil
}

func generateSecrets() secretsData {
	return secretsData{
		postgresPassword:          randPassword(28),
		sessionPepper:             randB64(32),
		encryptionKey:             randB64(32),
		internalToken:             randB64(32),
		gatewayTokenSecret:        randB64(32),
		connectorOAuthStateSecret: randB64(32),
		adminPassword:             randPassword(20),
		rootPassword:              randPassword(24),
	}
}

func randB64(length int) string {
	buf := make([]byte, length)
	if _, err := rand.Read(buf); err != nil {
		panic(err)
	}
	return base64.StdEncoding.EncodeToString(buf)
}

func randPassword(length int) string {
	const alphabet = "abcdefghijkmnopqrstuvwxyzABCDEFGHJKLMNPQRSTUVWXYZ23456789-_"
	var out strings.Builder
	for out.Len() < length {
		n, err := rand.Int(rand.Reader, big.NewInt(int64(len(alphabet))))
		if err != nil {
			panic(err)
		}
		out.WriteByte(alphabet[n.Int64()])
	}
	return out.String()
}

func (c *doClient) request(ctx context.Context, method string, path string, body any, out any) error {
	var reader io.Reader
	if body != nil {
		raw, err := json.Marshal(body)
		if err != nil {
			return err
		}
		reader = bytes.NewReader(raw)
	}
	req, err := http.NewRequestWithContext(ctx, method, doAPIBase+path, reader)
	if err != nil {
		return err
	}
	req.Header.Set("Authorization", "Bearer "+c.token)
	req.Header.Set("Accept", "application/json")
	if body != nil {
		req.Header.Set("Content-Type", "application/json")
	}
	resp, err := c.http.Do(req)
	if err != nil {
		return err
	}
	defer resp.Body.Close()
	raw, _ := io.ReadAll(resp.Body)
	if resp.StatusCode < 200 || resp.StatusCode >= 300 {
		var payload struct {
			Message string `json:"message"`
			ID      string `json:"id"`
		}
		_ = json.Unmarshal(raw, &payload)
		message := strings.TrimSpace(payload.Message)
		if message == "" {
			message = strings.TrimSpace(payload.ID)
		}
		if message == "" {
			message = string(raw)
		}
		return fmt.Errorf("DigitalOcean API error (%d): %s", resp.StatusCode, message)
	}
	if out == nil || len(raw) == 0 {
		return nil
	}
	return json.Unmarshal(raw, out)
}

